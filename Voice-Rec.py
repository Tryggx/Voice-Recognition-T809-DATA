class IterMeter(object):
    """keeps track of total iterations"""
    def __init__(self):
        self.val = 0

    def step(self):
        self.val += 1

    def get(self):
        return self.val



def train(model, device, train_loader, criterion, optimizer, scheduler, epoch, iter_meter, writer):
    model.train()
    data_len = len(train_loader.dataset)
    torch.autograd.set_detect_anomaly(True)
    total_loss = 0
    #with experiment.train():
    print('data_len', data_len)
    for batch_idx, _data in enumerate(train_loader):
            print('batch_idx', batch_idx)
            spectrograms, labels, input_lengths, label_lengths = _data 
            spectrograms, labels = spectrograms.to(device), labels.to(device)

            optimizer.zero_grad()
           
            output = model(spectrograms)  # (batch, time, n_class)
            output = F.log_softmax(output, dim=2)
            output = output.transpose(0, 1) # (time, batch, n_class)
            
            loss = criterion(output, labels, input_lengths, label_lengths)
            loss.backward()
            total_loss += loss.item() * spectrograms.size(0)  # Accumulate loss
            average_loss = total_loss / data_len

            optimizer.step()
            scheduler.step()
            iter_meter.step()

            writer.add_scalar('Loss/train_step', loss.item(), iter_meter.get())
            writer.add_scalar('Learning_rate/train_step', scheduler.get_lr()[0], iter_meter.get())

            if batch_idx % 100 == 0 or batch_idx == data_len:
                print('Train Epoch: {} [{}/{} ({:.0f}%)]\tLoss: {:.6f}'.format(
                    epoch, batch_idx * len(spectrograms), data_len,
                    100. * batch_idx / len(train_loader), loss.item()))
    return average_loss
                


